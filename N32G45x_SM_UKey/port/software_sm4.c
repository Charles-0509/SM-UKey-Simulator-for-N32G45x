static uint32_t get_be32(const uint8_t *p)
{
    return ((uint32_t)p[0] << 24) | ((uint32_t)p[1] << 16) | ((uint32_t)p[2] << 8) | p[3];
}

static void put_be32(uint8_t *p, uint32_t x)
{
    p[0] = (uint8_t)(x >> 24);
    p[1] = (uint8_t)(x >> 16);
    p[2] = (uint8_t)(x >> 8);
    p[3] = (uint8_t)x;
}

static uint32_t tau(uint32_t x)
{
    return ((uint32_t)S[(x >> 24) & 0xffu] << 24) |
           ((uint32_t)S[(x >> 16) & 0xffu] << 16) |
           ((uint32_t)S[(x >> 8) & 0xffu] << 8) |
           S[x & 0xffu];
}

static uint32_t l1(uint32_t x)
{
    return x ^ ROL32(x, 2) ^ ROL32(x, 10) ^ ROL32(x, 18) ^ ROL32(x, 24);
}

static uint32_t l2(uint32_t x)
{
    return x ^ ROL32(x, 13) ^ ROL32(x, 23);
}

static void set_key(const uint8_t key[16], uint32_t rk[32])
{
    uint32_t k[36];
    uint32_t i;
    for (i = 0u; i < 4u; i++) {
        k[i] = get_be32(key + 4u * i) ^ FK[i];
    }
    for (i = 0u; i < 32u; i++) {
        k[i + 4u] = k[i] ^ l2(tau(k[i + 1u] ^ k[i + 2u] ^ k[i + 3u] ^ CK[i]));
        rk[i] = k[i + 4u];
    }
}

static void crypt_block(const uint8_t in[16], uint8_t out[16], const uint32_t rk[32], int enc)
{
    uint32_t x[36];
    uint32_t i;
    for (i = 0u; i < 4u; i++) {
        x[i] = get_be32(in + 4u * i);
    }
    for (i = 0u; i < 32u; i++) {
        uint32_t r = enc ? rk[i] : rk[31u - i];
        x[i + 4u] = x[i] ^ l1(tau(x[i + 1u] ^ x[i + 2u] ^ x[i + 3u] ^ r));
    }
    put_be32(out, x[35]);
    put_be32(out + 4, x[34]);
    put_be32(out + 8, x[33]);
    put_be32(out + 12, x[32]);
}

void software_sm4_crypt_cbc(const uint8_t key[16], const uint8_t iv[16],
                            const uint8_t *in, uint8_t *out,
                            uint32_t len, int encrypt)
{
    uint32_t rk[32];
    uint8_t prev[16];
    uint8_t block[16];
    uint8_t tmp[16];
    uint32_t off;
    uint32_t i;

    set_key(key, rk);
    memcpy(prev, iv, 16u);
    for (off = 0u; off < len; off += 16u) {
        if (encrypt) {
            for (i = 0u; i < 16u; i++) {
                block[i] = in[off + i] ^ prev[i];
            }
            crypt_block(block, out + off, rk, 1);
            memcpy(prev, out + off, 16u);
        } else {
            memcpy(tmp, in + off, 16u);
            crypt_block(in + off, block, rk, 0);
            for (i = 0u; i < 16u; i++) {
                out[off + i] = block[i] ^ prev[i];
            }
            memcpy(prev, tmp, 16u);
        }
    }
}
